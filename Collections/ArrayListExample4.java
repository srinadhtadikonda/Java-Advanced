//print elements using foreach loop
import java.lang.*;
import java.util.*;
class ArrayListExample4
{
public static void main(String args[])
{
ArrayList<String> AT=new ArrayList<String>();
AT.add("CAT");
AT.add("MAT");
AT.add("BASKET");
AT.add("APPLE");
AT.add("RAT");
AT.add("BALL");
for(String x:AT)
{
System.out.print(x+"\t");
}
}
}
